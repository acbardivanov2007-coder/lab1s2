// Подключаем наши заголовочные файлы
#include "bitstruct.h" // Для браслета
#include "datatime.h"  // Для работы с датами

// Главная функция - именно отсюда стартует программа
int main() {
   // Создаем структуру с текущей датой (12 апреля 2026, 17:44).
   // Важно: внутри этой функции datatime_create АВТОМАТИЧЕСКИ создается 
   // и наше устройство (браслет). Нам не нужно делать это вручную.
   datatime* d = datatime_create(12, 4, 2026, 17, 44);
   
   // Если память не выделилась - аварийно завершаем программу
   if (d == NULL) {
      return 1;
   }

   // Переменная для хранения выбора пользователя на каждом этапе
   uint16_t choice;
   // Переменная-копилка, куда мы будем плюсовать стоимость компонентов
   int total_price = 0;

   // Выводим приветствие
   printf("=== Конструктор Умного Фитнес-Браслета ===\n");
   
   // Демонстрируем, как работает выравнивание памяти (Задание на 4).
   // sizeof показывает, сколько реальных байт занимает наша структура в оперативной памяти компьютера.
   printf("Размер структуры device_data в памяти: %zu байт(а)\n", sizeof(device_data));

   // --- ВЫБОР ДИСПЛЕЯ ---
   printf("\nВыберите дисплей:\n");
   printf("0 - LCD      (1000 руб)\n");
   printf("1 - LED      (1500 руб)\n");
   printf("2 - OLED     (3000 руб)\n");
   printf("3 - E-Ink    (2500 руб)\n");
   printf("Ваш выбор: ");
   
   // Просим ввести число от 0 до 3
   choice = get_choice(0, 3);

   // Сохраняем выбор в наше устройство, которое "спрятано" внутри структуры даты (d->dev)
   dev_set_display(d->dev, choice);
   
   // Прибавляем к общей цене стоимость выбранного дисплея (берем из массива display_price)
   total_price += display_price[choice];

   // --- ВЫБОР ЯРКОСТИ ---
   printf("\nЯркость (0-15): ");
   choice = get_choice(0, 15); // Запрашиваем число от 0 до 15

   dev_set_brightness(d->dev, choice); // Записываем яркость
   // Допустим, каждый уровень яркости стоит 50 рублей
   total_price += choice * 50;  

   // --- ВЫБОР ФОРМАТА ВРЕМЕНИ ---
   printf("\nФормат времени:\n");
   printf("0 - 24 часа\n");
   printf("1 - 12 часов\n");
   printf("Ваш выбор: ");
   choice = get_choice(0, 1);

   dev_set_time_format(d->dev, choice);
   // Цена за это не меняется

   // --- ВЫБОР БУДИЛЬНИКА ---
   printf("\nБудильник:\n");
   printf("0 - Нет\n");
   printf("1 - Да (+500 руб)\n");
   printf("Ваш выбор: ");
   choice = get_choice(0, 1);

   dev_set_alarm(d->dev, choice); // Сохраняем настройку
   // Если пользователь выбрал "1" (Да), то накидываем 500 рублей к итоговой цене
   if (choice == 1) 
      total_price += 500;

   // --- ВЫБОР ПАМЯТИ ---
   printf("\nПамять:\n");
   printf("0 - 16KB (500 руб)\n");
   printf("1 - 32KB (1000 руб)\n");
   printf("2 - 64KB (2000 руб)\n");
   printf("3 - 128KB (3000 руб)\n");
   printf("Ваш выбор: ");
   choice = get_choice(0, 3);

   dev_set_memory(d->dev, choice);
   total_price += memory_price[choice]; // Добавляем цену из прайса памяти

   // --- ВЫБОР ПРОЦЕССОРА ---
   printf("\nПроцессор:\n");
   printf("0 - ARM     (2000 руб)\n");
   printf("1 - AVR     (1500 руб)\n");
   printf("2 - M-16pro (2500 руб)\n");
   printf("3 - x86     (4000 руб)\n");
   printf("Ваш выбор: ");
   choice = get_choice(0, 3);

   dev_set_cpu(d->dev, choice);
   total_price += cpu_price[choice]; // Добавляем цену из прайса процессоров

   // --- ВЫБОР ВОДОСТОЙКОСТИ ---
   printf("\nВодостойкость:\n");
   printf("0 - Нет\n");
   printf("1 - IP67 (+800 руб)\n");
   printf("2 - IP68 (+1500 руб)\n");
   printf("Ваш выбор: ");
   choice = get_choice(0, 2);

   dev_set_water(d->dev, choice); // Записываем влагозащиту

   // Вручную прибавляем стоимость в зависимости от выбора
   if (choice == 1) total_price += 800;
   if (choice == 2) total_price += 1500;

   // --- ФИНАЛЬНЫЙ ВЫВОД РЕЗУЛЬТАТОВ ---
   printf("\n=== Ваш Фитнес-Браслет собран ===\n");

   // Читаем номер дисплея (0-3) через геттер, и подставляем его в массив названий, 
   // чтобы вывести на экран слово (например, "OLED"), а не просто цифру "2".
   printf("Дисплей: %s\n", 
      display_names[dev_get_display(d->dev)]);

   // Яркость выводим просто как число (%u означает беззнаковое целое число)
   printf("Яркость: %u\n", 
      dev_get_brightness(d->dev));

   // Здесь используется так называемый "тернарный оператор" (условие ? если_да : если_нет).
   // Если формат равен 0, выводим "24 часа", иначе "12 часов".
   printf("Формат времени: %s\n", 
      dev_get_time_format(d->dev) == 0 ? "24 часа" : "12 часов");

   printf("Будильник: %s\n", 
      dev_get_alarm(d->dev) == 0 ? "Выключен" : "Включен");

   printf("Память: %s\n", 
      memory_names[dev_get_memory(d->dev)]);

   printf("Процессор: %s\n", 
      cpu_names[dev_get_cpu(d->dev)]);

   printf("Водостойкость: %s\n", 
      water_names[dev_get_water(d->dev)]);
    
   // Выводим финальную посчитанную сумму
   printf("\nИТОГОВАЯ ЦЕНА: %d руб\n", total_price);

   // ВАЖНО: Очищаем выделенную память!
   // Функция datatime_destroy не только удаляет структуру с датой, 
   // но и внутри себя вызывает dev_destroy, удаляя наш собранный браслет.
   datatime_destroy(d);

   // return 0 говорит операционной системе, что программа завершилась без ошибок
   return 0;
}
