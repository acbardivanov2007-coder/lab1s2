// Подключаем наш написанный модуль, чтобы иметь доступ к функциям дат
#include "datatime.h"

int main(void) {
    // Просто выводим красивый заголовок в консоль
    printf(" Выполнение практической работы 1 \n");
    printf("Тема: Структура данных 'datatime'\n\n");

    // 1. Создание объектов (Оценка: 3 балла)
    
    // Создаем первую дату в памяти. Данные актуальные: 12 апреля 2026 года, 17:13
    // Функция malloc внутри выделила под dt1 кусочек памяти.
    datatime *dt1 = datatime_create(12, 4, 2026, 17, 13);
    
    // Создаем вторую дату в памяти. 12 апреля 2026 года, 17:15 (на две минуты позже)
    datatime *dt2 = datatime_create(12, 4, 2026, 17, 15);

    printf("Сгенерированные даты:\n");
    // Вызываем нашу функцию вывода. Она возьмет данные из оперативной памяти и напечатает.
    datatime_print(dt1);
    datatime_print(dt2);
    printf("\n");

    // 2. Сравнение дат 
    
    // Сравниваем первую и вторую дату
    int compare_res = datatime_compare(dt1, dt2);
    
    if (compare_res < 0) { // Если первая меньше (раньше)
        // Считаем разницу, переведя всё в минуты для удобства, и выводим
        printf("Первая дата предшествует второй. Разница: %llu минут(ы)\n", datatime_to_minutes(dt2) - datatime_to_minutes(dt1));
    } else if (compare_res > 0) { // Если вторая меньше (раньше)
        printf("Вторая дата предшествует первой. Разница: %llu минут(ы)\n", datatime_to_minutes(dt1) - datatime_to_minutes(dt2));
    } else { // Если они равны (выпал 0)
        printf("Обе даты полностью совпадают.\n");
    }
    printf("\n");

    // Создаем "пустую" дату (везде будут нули), просто чтобы проверить, как работает конструктор по умолчанию
    datatime *empty_obj = create_empty_datatime();
    printf("Проверка пустого объекта:\n");
    datatime_print(empty_obj);
    
    // Копируем данные из нашей первой даты (dt1) в этот пустой объект
    copy_datatime(empty_obj, dt1);
    printf("Результат после работы конструктора копирования:\n");
    datatime_print(empty_obj); // Теперь здесь тоже 12 апреля 2026 17:13
    printf("\n");
    
    // Так как empty_obj нам больше не нужен, освобождаем память, чтобы она не "висела" просто так
    datatime_destroy(&empty_obj);

    // 3. Изменение полей 
    
    printf("Инкремент минут для первой даты (+1 мин):\n");
    // Вызываем функцию, которая добавит к 17:13 одну минуту. Станет 17:14
    datatime_min_increment(dt1);
    datatime_print(dt1);

    printf("Декремент минут для второй даты (-4 мин):\n");
    // С помощью цикла 'for' вызываем функцию уменьшения минут ровно 4 раза подряд
    // Было 17:15, станет 17:11
    for(int i = 0; i < 4; i++) {
        datatime_min_decrement(dt2);
    }
    datatime_print(dt2);
    printf("\n");

    printf("Использование сеттера: часы первой даты = 19:\n");
    // Принудительно ставим часы на 19. Минуты останутся как были (14). Итог: 19:14
    datatime_hour_set(dt1, 19); 
    datatime_print(dt1);
    printf("\n");

    // 4. Форматированный ввод/вывод из строки 
    
    // Превращаем нашу дату dt1 (12/04/2026 19:14) в обычную текстовую строку
    char *dt_string = datatime_to_string(dt1);
    // Проверяем, удалось ли создать строку
    if (dt_string != NULL) {
        // Печатаем её (%s означает 'вставь строку сюда')
        printf("Первая дата преобразована в строку: %s\n", dt_string);
        // Текстовая строка тоже занимает память (buffer), освобождаем её
        free(dt_string);
    }
    printf("\n");

    // Теперь, наоборот, берем текст и заставляем программу "прочитать" его и создать структуру
    // Создаем dt3 из текста
    datatime *dt3 = datatime_from_string("15/04/2026 12:00");
    printf("Дата успешно распарсена из строки \"15/04/2026 12:00\":\n");
    datatime_print(dt3);
    printf("\n");

    // 5. Интервал между датами
    
    // Вызываем функцию, которая красиво посчитает разницу по годам, дням, часам и минутам
    // Считаем между dt1 (12 апр, 19:14) и dt2 (12 апр, 17:11)
    printf("Подсчет разницы между первой и второй датой:\n");
    datatime_print_diff(dt1, dt2);
    printf("\n");

    // Очистка памяти в конце работы программы
    
    // Мы создавали dt1, dt2 и dt3 с помощью malloc.
    // Если мы закроем программу, не освободив память, это называется "утечка памяти".
    // Поэтому ОБЯЗАТЕЛЬНО удаляем каждую структуру перед выходом (return 0).
    datatime_destroy(&dt1);
    datatime_destroy(&dt2);
    datatime_destroy(&dt3);
    
    printf("Вся динамическая память освобождена. Завершение работы.\n");
    
    // return 0 говорит операционной системе, что программа выполнилась успешно, без сбоев.
    return 0;
}
